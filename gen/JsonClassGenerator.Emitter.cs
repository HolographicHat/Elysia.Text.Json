using System.Collections.Immutable;
using Elysia.Utilities;
using Microsoft.CodeAnalysis;

namespace Elysia.Text.Json;

public partial class JsonClassGenerator {

    private static void EmitSourceFile(
        SourceProductionContext context,
        (bool, (ImmutableArray<JsonSerializerContextInfo>, ImmutableArray<JsonClassInfo>)) infos
    ) {
        var (isPublishing, (serializerContexts, classes)) = infos;
        foreach (var cls in classes.Where(c => c.Diagnostic != null)) {
            context.ReportDiagnostic(cls.Diagnostic!);
        }
        if (serializerContexts.Length > 1) {
            // ReSharper disable once ForeachCanBePartlyConvertedToQueryUsingAnotherGetEnumerator
            foreach (var info in serializerContexts) {
                var diagnostic = Diagnostic.Create(DiagnosticDescriptors.MultipleSerializerContexts, info.Location);
                context.ReportDiagnostic(diagnostic);
            }
        }
        if (isPublishing) {
            return; // 
        }
        EmitJsonClassesFile(context, classes);
        if (serializerContexts.Length == 1) {
            EmitJsonSerializerContextFile(context, serializerContexts[0], classes);
        }
    }

    private static void EmitJsonClassesFile(SourceProductionContext context, ImmutableArray<JsonClassInfo> classes) {
        var sb = new IndentStringBuilder();
        sb.AppendLine("// <auto-generated/>\n");
        sb.AppendLine("#nullable enable\n");
        foreach (var types in classes.Where(cls => cls.Diagnostic == null).GroupBy(cls => cls.TypeRef.Namespace)) {
            if (types.Key != null) {
                sb.AppendLine("namespace {0} {{\n", types.Key);
                sb.AddIndentLevel();
            }
            foreach (var (_, withExt, typeInfo, _) in types) {
                foreach (var s in typeInfo.ContainingTypes) {
                    sb.AppendLine("partial {1} {0} {{\n", s.Name, s.Modifier);
                    sb.AddIndentLevel();
                }
                sb.AppendLine("partial {1} {0} : global::Elysia.Text.Json.IJsonSerializable{2}\n", typeInfo.Name, typeInfo.Modifier, withExt ? " {" : ";");
                if (withExt) {
                    sb.AddIndentLevel();
                    sb.AppendLine("[global::System.Text.Json.Serialization.JsonExtensionData]");
                    sb.AppendLine("public global::System.Collections.Generic.Dictionary<string, global::System.Text.Json.JsonElement> ExtensionData { get; set; } = [];\n");
                    sb.SubIndentLevel();
                    sb.AppendLine("}\n");
                }
                for (var i = 0; i < typeInfo.ContainingTypes.Count; i++) {
                    sb.SubIndentLevel();
                    sb.AppendLine("}\n");
                }
            }
            if (types.Key != null) {
                sb.SubIndentLevel();
                sb.AppendLine("}\n");
            }
        }
        context.AddSource($"{nameof(JsonClassGenerator)}.JsonClasses.g.cs", sb.ToString());
    }

    private static void EmitJsonSerializerContextFile(
        SourceProductionContext context,
        JsonSerializerContextInfo serializerContext,
        ImmutableArray<JsonClassInfo> classes
    ) {
        var serializableTypeNames = classes
            .Where(c => c.Diagnostic == null)
            .SelectMany(s => s.ExtraTypes.Append(s.TypeRef))
            .Select(t => t.FullyQualifiedName)
            .Distinct();
        var serializerContextTypeInfo = serializerContext.TypeRef;
        //
        var sb = new IndentStringBuilder();
        sb.AppendLine("// <auto-generated/>\n");
        sb.AppendLine("namespace {0} {{\n", serializerContextTypeInfo.Namespace);
        sb.AddIndentLevel();
        foreach (var s in serializerContextTypeInfo.ContainingTypes) {
            sb.AppendLine("partial {1} {0} {{\n", s.Name, s.Modifier);
            sb.AddIndentLevel();
        }
        sb.AppendLine("#if IS_PUBLISHING");
        foreach (var typeName in serializableTypeNames) {
            sb.AppendLine("[global::System.Text.Json.Serialization.JsonSerializable(typeof({0}))]", typeName);
        }
        sb.AppendLine("#endif");
        sb.AppendLine("partial class {0};\n", serializerContextTypeInfo.Name);
        for (var i = 0; i < serializerContextTypeInfo.ContainingTypes.Count; i++) {
            sb.SubIndentLevel();
            sb.AppendLine("}\n");
        }
        sb.SubIndentLevel();
        sb.AppendLine("}\n");
        //
        context.AddSource($"{nameof(JsonClassGenerator)}.JsonSerializerContext.g.cs", sb.ToString());
    }

}
